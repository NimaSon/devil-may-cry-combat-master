  static String tr(String key, {Map<String, String>? args}) {
    final map = locale.languageCode == 'en' ? _en : _ru;
    String res = map[key] ?? key;
    if (args != null) {
      args.forEach((k, v) => res = res.replaceAll('{$k}', v));
    }
    return res;
  }
}

String t(String key, [Map<String, String>? args]) => L10n.tr(key, args: args);